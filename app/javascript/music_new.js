const addMusic = document.getElementById('add-music')
const form = document.getElementById('add-music-form')

function show (){
  form.classList.remove("d-none");
  addMusic.removeEventListener('click', show)
  addMusic.addEventListener('click', remove)
}

function remove (){
  form.classList.add("d-none")
  addMusic.removeEventListener('click', remove)
  addMusic.addEventListener('click', show)
}

addMusic.addEventListener('click', show)